-- 데이터 불일치 분석 DDL/DML/운영분석 SQL
-- 대표 테이블: TBDISCREPANCY, snapshot 컬럼: REASON

CREATE TABLE TBDISCREPANCY (
    ID              VARCHAR(40)   NOT NULL,
    INSTCD          VARCHAR(20)   NOT NULL,
    USERID          VARCHAR(40),
    NAME            VARCHAR(100),
    REASON          VARCHAR(100),
    STATUS          VARCHAR(20),
    CREATED_BY      VARCHAR(40),
    UPDATED_BY      VARCHAR(40),
    CREATED_AT      TIMESTAMP,
    UPDATED_AT      TIMESTAMP,
    REVISION        INTEGER,
    CONSTRAINT PK_TBDISCREPANCY PRIMARY KEY (ID)
);

CREATE INDEX IX_TBDISCREPANCY_INSTCD ON TBDISCREPANCY(INSTCD);
CREATE INDEX IX_TBDISCREPANCY_STATUS ON TBDISCREPANCY(STATUS);
CREATE INDEX IX_TBDISCREPANCY_MISMATCH ON TBDISCREPANCY(INSTCD, STATUS, NAME, REASON);

