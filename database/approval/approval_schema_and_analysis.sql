-- 승인/반려 관리 DDL/DML/운영분석 SQL
-- 대표 테이블: TBAPPROVAL, snapshot 컬럼: APPROVERNM

CREATE TABLE TBAPPROVAL (
    ID              VARCHAR(40)   NOT NULL,
    INSTCD          VARCHAR(20)   NOT NULL,
    USERID          VARCHAR(40),
    NAME            VARCHAR(100),
    APPROVERNM      VARCHAR(100),
    STATUS          VARCHAR(20),
    CREATED_BY      VARCHAR(40),
    UPDATED_BY      VARCHAR(40),
    CREATED_AT      TIMESTAMP,
    UPDATED_AT      TIMESTAMP,
    REVISION        INTEGER,
    CONSTRAINT PK_TBAPPROVAL PRIMARY KEY (ID)
);

CREATE INDEX IX_TBAPPROVAL_INSTCD ON TBAPPROVAL(INSTCD);
CREATE INDEX IX_TBAPPROVAL_STATUS ON TBAPPROVAL(STATUS);
CREATE INDEX IX_TBAPPROVAL_MISMATCH ON TBAPPROVAL(INSTCD, STATUS, NAME, APPROVERNM);

