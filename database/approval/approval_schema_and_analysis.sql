-- 제출완료 기관 중 현재명과 snapshot명이 다른 건 추출
SELECT INSTCD, USERID, NAME AS CURRENT_NAME, APPROVERNM AS SNAPSHOT_NAME, STATUS
  FROM TBAPPROVAL
 WHERE STATUS = 'SUBMITTED'
   AND NVL(NAME, ' ') <> NVL(APPROVERNM, ' ');

-- TBUSER와의 비교. 운영 문의 예시에서 가장 자주 쓰는 형태.
SELECT A.INSTCD, U.USERNM, A.APPROVERNM, A.STATUS, A.UPDATED_AT
  FROM TBAPPROVAL A
  JOIN TBUSER U
    ON A.INSTCD = U.INSTCD
 WHERE A.STATUS = 'SUBMITTED'
   AND NVL(U.USERNM, ' ') <> NVL(A.APPROVERNM, ' ');

-- 분석 SQL 1: 특정 기관 기준 불일치 및 변경 이력 추적
SELECT A.INSTCD, A.ID, A.NAME, A.APPROVERNM, A.STATUS, A.UPDATED_BY, A.UPDATED_AT,
       H.CHANGEDESC, H.CREATED_AT AS HISTORY_AT
  FROM TBAPPROVAL A
  LEFT JOIN TBHISTORY H
    ON A.ID = H.ID
 WHERE A.STATUS = 'SUBMITTED'
   AND NVL(A.NAME, ' ') <> NVL(A.APPROVERNM, ' ')
   AND A.INSTCD IN ('INST001','INST002','INST003','INST004')
 ORDER BY A.INSTCD, H.CREATED_AT DESC;

