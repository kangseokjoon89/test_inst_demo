-- 1단계: 제출완료 데이터만 확인
SELECT A.INSTCD, A.ID, A.ACTIONUSERNM, A.STATUS, A.SUBMIT_DT, A.CHANGE_DT
  FROM TBAUDITHIST A
 WHERE A.STATUS = 'SUBMITTED'
 ORDER BY A.INSTCD;

-- 2단계: TBUSER 현재 담당자명과 snapshot 값을 JOIN으로 비교
SELECT A.INSTCD, U.USERNM, A.ACTIONUSERNM AS SNAPSHOT_VALUE, A.STATUS, A.SOURCE_REVISION
  FROM TBAUDITHIST A
  JOIN TBUSER U
    ON A.INSTCD = U.INSTCD
 WHERE A.STATUS = 'SUBMITTED'
   AND NVL(U.USERNM, ' ') <> NVL(A.ACTIONUSERNM, ' ')
 ORDER BY A.INSTCD;

-- 3단계: 변경 이력과 연결
SELECT A.INSTCD, U.USERNM, A.ACTIONUSERNM, H.CHANGEDESC, H.CREATED_AT
  FROM TBAUDITHIST A
  JOIN TBUSER U ON A.INSTCD = U.INSTCD
  LEFT JOIN TBHISTORY H ON A.ID = H.TARGET_ID
 WHERE A.STATUS = 'SUBMITTED'
   AND NVL(U.USERNM, ' ') <> NVL(A.ACTIONUSERNM, ' ')
 ORDER BY A.INSTCD, H.CREATED_AT DESC;

