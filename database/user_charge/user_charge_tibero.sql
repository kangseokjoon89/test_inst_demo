-- 담당자 기준정보 Tibero 운영 분석 SQL
-- 테이블: TBUSER, 비교 컬럼: USERNM
-- 서브쿼리를 쓰지 않고 JOIN과 조건 분해로 확인한다.

CREATE TABLE TBUSER (
    ID              VARCHAR(40)    NOT NULL,
    INSTCD          VARCHAR(20)    NOT NULL,
    USERID          VARCHAR(40),
    USERNM          VARCHAR(100),
    STATUS          VARCHAR(20),
    SUBMIT_DT       DATE,
    CHANGE_DT       DATE,
    BATCH_YN        CHAR(1),
    SKIP_REASON     VARCHAR(200),
    SOURCE_REVISION VARCHAR(40),
    SCREEN_ID       VARCHAR(80),
    TRANSACTION_ID  VARCHAR(80),
    UPDATED_BY      VARCHAR(40),
    MEMO            VARCHAR(500),
    CONSTRAINT PK_TBUSER PRIMARY KEY (ID)
);

CREATE INDEX IX_TBUSER_01 ON TBUSER(INSTCD, STATUS);
CREATE INDEX IX_TBUSER_02 ON TBUSER(TRANSACTION_ID, SCREEN_ID);
CREATE INDEX IX_TBUSER_03 ON TBUSER(SOURCE_REVISION, CHANGE_DT);

