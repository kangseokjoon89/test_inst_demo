-- 1단계: 제출완료 데이터만 확인
SELECT A.INSTCD, A.ID, A.SYNCTARGETNM, A.STATUS, A.SUBMIT_DT, A.CHANGE_DT
  FROM TBBATCHSYNC A
 WHERE A.STATUS = 'SUBMITTED'
 ORDER BY A.INSTCD;

-- 2단계: TBUSER 현재 담당자명과 snapshot 값을 JOIN으로 비교
SELECT A.INSTCD, U.USERNM, A.SYNCTARGETNM AS SNAPSHOT_VALUE, A.STATUS, A.SOURCE_REVISION
  FROM TBBATCHSYNC A
  JOIN TBUSER U
    ON A.INSTCD = U.INSTCD
 WHERE A.STATUS = 'SUBMITTED'
   AND NVL(U.USERNM, ' ') <> NVL(A.SYNCTARGETNM, ' ')
 ORDER BY A.INSTCD;

-- 3단계: 변경 이력과 연결
SELECT A.INSTCD, U.USERNM, A.SYNCTARGETNM, H.CHANGEDESC, H.CREATED_AT
  FROM TBBATCHSYNC A
  JOIN TBUSER U ON A.INSTCD = U.INSTCD
  LEFT JOIN TBHISTORY H ON A.ID = H.TARGET_ID
 WHERE A.STATUS = 'SUBMITTED'
   AND NVL(U.USERNM, ' ') <> NVL(A.SYNCTARGETNM, ' ')
 ORDER BY A.INSTCD, H.CREATED_AT DESC;

-- 진단 SQL 1: 배치실행관리 / 동기화 배치 실행 / trace 1
SELECT A.INSTCD, A.ID, U.USERNM AS CURRENT_VALUE, A.SYNCTARGETNM AS SAVED_VALUE,
       A.SUBMIT_DT, A.CHANGE_DT, A.BATCH_YN, A.SKIP_REASON, A.SOURCE_REVISION,
       A.SCREEN_ID, A.TRANSACTION_ID, A.UPDATED_BY
  FROM TBBATCHSYNC A
  JOIN TBUSER U ON A.INSTCD = U.INSTCD
  LEFT JOIN TBAI_OPERATOR_TRACE T ON A.SCREEN_ID = T.SCREEN_ID
 WHERE A.STATUS = 'SUBMITTED'
   AND A.TRANSACTION_ID = 'batch_sync_tx_1'
   AND NVL(U.USERNM, ' ') <> NVL(A.SYNCTARGETNM, ' ')
 ORDER BY A.CHANGE_DT DESC;

