-- 배치 동기화 Tibero 운영 분석 SQL
-- 테이블: TBBATCHSYNC, 비교 컬럼: SYNCTARGETNM
-- 서브쿼리를 쓰지 않고 JOIN과 조건 분해로 확인한다.

CREATE TABLE TBBATCHSYNC (
    ID              VARCHAR(40)    NOT NULL,
    INSTCD          VARCHAR(20)    NOT NULL,
    USERID          VARCHAR(40),
    SYNCTARGETNM    VARCHAR(100),
    STATUS          VARCHAR(20),
    SUBMIT_DT       DATE,
    CHANGE_DT       DATE,
    BATCH_YN        CHAR(1),
    SKIP_REASON     VARCHAR(200),
    SOURCE_REVISION VARCHAR(40),
    SCREEN_ID       VARCHAR(80),
    TRANSACTION_ID  VARCHAR(80),
    UPDATED_BY      VARCHAR(40),
    MEMO            VARCHAR(500),
    CONSTRAINT PK_TBBATCHSYNC PRIMARY KEY (ID)
);

CREATE INDEX IX_TBBATCHSYNC_01 ON TBBATCHSYNC(INSTCD, STATUS);
CREATE INDEX IX_TBBATCHSYNC_02 ON TBBATCHSYNC(TRANSACTION_ID, SCREEN_ID);
CREATE INDEX IX_TBBATCHSYNC_03 ON TBBATCHSYNC(SOURCE_REVISION, CHANGE_DT);

